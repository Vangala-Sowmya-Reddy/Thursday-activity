class ArithmeticOperators{
public static void main(String[] args)
{
int a=10,b=20,c=30;
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a%b);
System.out.println(a*b);
System.out.println(a>b && b<c);
System.out.println(a>c & b<c);
System.out.println(a>c || b<a);
System.out.println(a>b | b<c);
}
}